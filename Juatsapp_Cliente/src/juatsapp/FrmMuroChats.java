    public void actualizarChats() {
        PanelMuro.removeAll();
        ChatDAO pd = new ChatDAO();
        List<Chat> chats = pd.mostrarTodas();
        Collections.reverse(chats);
        for (Chat chat : chats) {
            PanelMuro.add(new PanelChat(usuarioConectado, chat, this));
        }
        //  PanelMuro.doLayout();
    }

    private void llenarListaChats(){
        ChatDAO pd = new ChatDAO();
        List<Chat> chats = pd.mostrarTodas();
        for (Chat chat : chats) {
            cbChats.addItem(chat.getTituloChat());
        }        
    }
