    private void bBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscarActionPerformed
        if (tfBuscar.getText().isEmpty()) {
            actualizarChats();
        } else {
            PanelMuro.removeAll();
            ChatDAO pd = new ChatDAO();
            List<Chat> chats = pd.mostrarPorIntegrantes(tfBuscar.getText());
            
            if (!chats.isEmpty()) {
            Collections.reverse(chats);    
            for (Chat chat : chats) {
                PanelMuro.add(new PanelChat(usuarioConectado, chat, this));
            }
            } else {
                JOptionPane.showMessageDialog(this, "No se encontro ningún chat con el integrante buscado", "Aviso", INFORMATION_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_bBuscarActionPerformed

