    public void agregarContenido() {

        UsuarioDAO ud = new UsuarioDAO();
        Usuario autor = ud.buscarPorId(chat.getAutor());
        lbAutor.setText(autor.getEmailUsuario());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MMM/yyyy HH:mm");
        String fecha = formato.format(chat.getFechaHora());        
        lbfecha.setText(fecha);
        Object[] integrantes = chat.getIntegrantes().toArray();

        String integrantesConvertidos = "";
        for (int i = 0; i < integrantes.length; i++) {
            if (i == integrantes.length - 1) {
                integrantesConvertidos += integrantes[i].toString();
            } else {
                integrantesConvertidos = integrantes[i] + ",";
            }
        }
        lbIntegrantes.setText(integrantesConvertidos);
//        taChat.append(chat.getTituloChat());
        lblTitulo.setText(chat.getTituloChat());
        if (usuarioConectado.getId().equals(chat.getAutor())) {
            bEliminar.setVisible(true);
            bEliminar.setEnabled(true);
        } else {
            bEliminar.setVisible(false);
            bEliminar.setEnabled(false);
        }

        if (!chat.getMensajes().isEmpty()) {
            agregarMensajes();
        }

    }

    public void agregarMensajes() {

        if (!chat.getMensajes().isEmpty()) {
            List<Mensaje> mensajes = chat.getMensajes();
            for (Mensaje mensaje : mensajes) {
                pMensajes.add(new PanelMensaje(mensaje, usuarioConectado,chat,muro));
            }
        } else {
            System.out.println("");
        }
    }

